    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         JOptionPane.showMessageDialog(null, "Salvar os Dados?", 
                "Cadastro de Post", JOptionPane.QUESTION_MESSAGE);
                 
         content = jTextField1.getText();
         path = jTextField2.getText();
         if (path.equals("")) {
                    Post post = user.post(content);
                    posts.add(post);
                    post.save();
                } else {
                    ImagePost post = user.postImage(content, path);
                    imagePosts.add(post);
                    post.save();
                }

                
         user.save();
         Project home = new Project();
         home.setVisible(true);
         dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

