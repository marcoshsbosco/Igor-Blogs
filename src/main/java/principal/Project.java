    public Project() {
        initComponents();
        DefaultListModel<String> model = new DefaultListModel();

        File dir = new File("data/users/");
        files = dir.list();
        

        if (files != null) {
            user.load(files[0]);
        } else {
            user.save();
        }

        dir = new File("data/posts/");
        files = dir.list();
        
        if (files != null) {
            for (String filename : files) {
                if (filename.charAt(0) != 'i'){
                    Post post = new Post(0, "");
                    post.load(filename);
                    posts.add(post);
                } else {
                    ImagePost post = new ImagePost(0, "", "");
                    post.load(filename);
                    imagePosts.add(post);
                }
            }
        }
        
        for (Post post : posts) {
                    if (post.getModerated() == true) {
                        post.info();
                        jList1.setModel(new javax.swing.AbstractListModel<String>() {
                        String[] strings = {post.content};
                        public int getSize() { return strings.length; }
                        public String getElementAt(int i) { return strings[i]; }
        });
                    }else{
                      model.addElement(post.content);
                    }
        }
        
         for (Post imagePost : imagePosts) {
                    if (imagePost.getModerated() == true) {
                        imagePost.info();
                        jList1.setModel(new javax.swing.AbstractListModel<String>() {
                        String[] strings = {imagePost.content};
                        public int getSize() { return strings.length; }
                        public String getElementAt(int i) { return strings[i]; }
        });
                    }else{
                      model.addElement(imagePost.content);
                    }
        }
         
        jList1.setModel(model);

    }

